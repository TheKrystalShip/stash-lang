/// <summary>
/// Deterministic Markdown generator for the standard library reference.
/// The generator reads <see cref="StdlibDefinitions"/> metadata and writes
/// canonical Markdown. Running the generator twice without metadata
/// changes produces byte-identical output, so a clean-worktree git diff
/// check is the only CI signal required.
/// </summary>
public static class ReferenceGenerator
{
    private const string DocTitle = "Stash — Standard Library Reference";

    /// <summary>The relative path the generator writes when no override is supplied.</summary>
    public const string DefaultRelativePath = "docs/Stash — Standard Library Reference.md";

    public static string Generate()
    {
        var sb = new StringBuilder();
        // Ordered, capability-All view: the user-facing reference documents
        // every namespace the runtime knows about, regardless of capability gate.
        var namespaces = StdlibDefinitions.GetNamespaces(StashCapabilities.All);
        var globalNs = namespaces.FirstOrDefault(n => n.IsGlobal);
        var nonGlobal = namespaces.Where(n => !n.IsGlobal).ToArray();

        EmitFrontMatter(sb);
        EmitTableOfContents(sb, nonGlobal);
        EmitHowToRead(sb);
        EmitGlobalSection(sb, globalNs);
        foreach (var ns in nonGlobal)
        {
            EmitNamespace(sb, ns);
        }
        EmitAppendices(sb, namespaces);

        return Normalize(sb.ToString());
    }

    public static void WriteTo(string absolutePath)
    {
        var output = Generate();
        // Force LF endings and no BOM, matching repo convention.
        File.WriteAllText(absolutePath, output, new UTF8Encoding(encoderShouldEmitUTF8Identifier: false));
    }

