    private static void EmitTableOfContents(StringBuilder sb, IReadOnlyList<NamespaceDefinition> namespaces)
    {
        sb.AppendLine("## Table of Contents");
        sb.AppendLine();
        sb.AppendLine("1. [How to Read This Reference](#how-to-read-this-reference)");
        sb.AppendLine("2. [Global Functions and Types](#global-functions-and-types)");

        int idx = 3;
        foreach (var ns in namespaces)
        {
            string heading = NamespaceHeading(ns);
            string anchor = HeadingAnchor(heading);
            sb.Append(idx++).Append(". [`").Append(ns.Name).Append("`](#").Append(anchor).AppendLine(")");
        }

        sb.Append(idx++).AppendLine(". [Appendix A — Deprecated Members](#appendix-a--deprecated-members)");
        sb.Append(idx).AppendLine(". [Appendix B — Capability-Gated Namespaces](#appendix-b--capability-gated-namespaces)");
        sb.AppendLine();
        sb.AppendLine("---");
        sb.AppendLine();
    }

