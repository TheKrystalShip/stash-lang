    private static string CapabilityLabel(StashCapabilities cap)
    {
        if (cap == StashCapabilities.None) return "_none — always available_";
        return "`" + cap.ToString() + "`";
    }

    private static SortedSet<string> AggregateThrows(NamespaceDefinition ns)
    {
        var set = new SortedSet<string>(StringComparer.Ordinal);
        foreach (var fn in ns.Functions)
        {
            if (fn.Throws is null) continue;
            foreach (var t in fn.Throws)
                set.Add(t.ErrorType);
        }
        return set;
    }

    // ── Types and constants ──────────────────────────────────────────────────

    private static void EmitTypesSection(StringBuilder sb, IReadOnlyList<BuiltInStruct> structs, IReadOnlyList<BuiltInEnum> enums, int headingLevel)
    {
        if (structs.Count == 0 && enums.Count == 0) return;

        string hashes = new('#', headingLevel);
        sb.Append(hashes).AppendLine(" Types");
        sb.AppendLine();

        foreach (var s in structs)
        {
            sb.Append(hashes).Append("# `").Append(s.Name).AppendLine("` struct");
            sb.AppendLine();
            if (!string.IsNullOrEmpty(s.Description))
            {
                sb.AppendLine(s.Description);
                sb.AppendLine();
            }
            sb.AppendLine("| Field | Type |");
            sb.AppendLine("| ----- | ---- |");
            foreach (var f in s.Fields)
            {
                sb.Append("| `").Append(f.Name).Append("` | ").Append(FormatType(f.Type)).AppendLine(" |");
            }
            sb.AppendLine();
        }

        foreach (var e in enums)
        {
            sb.Append(hashes).Append("# `").Append(e.Name).AppendLine("` enum");
            sb.AppendLine();
            if (!string.IsNullOrEmpty(e.Description))
            {
                sb.AppendLine(e.Description);
                sb.AppendLine();
            }
            sb.Append("Members: ");
            sb.AppendLine(string.Join(", ", e.Members.Select(m => "`" + m + "`")));
            sb.AppendLine();
        }
    }

    private static void EmitConstantsSection(StringBuilder sb, IReadOnlyList<NamespaceConstant> constants)
    {
        if (constants.Count == 0) return;

        sb.AppendLine("### Constants");
        sb.AppendLine();
        sb.AppendLine("| Name | Type | Value | Description |");
        sb.AppendLine("| ---- | ---- | ----- | ----------- |");

        foreach (var c in constants)
        {
            var doc = DocString.Parse(c.Documentation);
            string desc = doc.SummaryFirstSentence();
            if (c.Deprecation is not null)
            {
                string suffix = $"_Deprecated — use `{c.Deprecation.ReplacementQualifiedName}`._";
                desc = string.IsNullOrEmpty(desc) ? suffix : desc + " " + suffix;
            }
            sb.Append("| `").Append(c.QualifiedName).Append("` | ")
              .Append(FormatType(c.Type)).Append(" | ")
              .Append(InlineCodeIfShort(c.Value)).Append(" | ")
              .Append(EscapeTableCell(desc)).AppendLine(" |");
        }
        sb.AppendLine();
    }

    // ── Functions ────────────────────────────────────────────────────────────

    private static void EmitFunctionSummaryTable(StringBuilder sb, IReadOnlyList<NamespaceFunction> functions, bool qualified)
    {
        sb.AppendLine("| Function | Returns | Throws | Description |");
        sb.AppendLine("| -------- | ------- | ------ | ----------- |");
        foreach (var fn in functions)
        {
            var doc = DocString.Parse(fn.Documentation);
            string desc = doc.SummaryFirstSentence();
            if (fn.Deprecation is not null)
            {
                string suffix = $"_Deprecated — use `{fn.Deprecation.ReplacementQualifiedName}`._";
                desc = string.IsNullOrEmpty(desc) ? suffix : desc + " " + suffix;
            }
            sb.Append("| ").Append(qualified ? "`" + fn.QualifiedName + "`" : "`" + fn.Name + "`")
              .Append(" | ").Append(FormatType(fn.ReturnType))
              .Append(" | ").Append(ThrowsCell(fn.Throws))
              .Append(" | ").Append(EscapeTableCell(desc)).AppendLine(" |");
        }
        sb.AppendLine();
    }

    private static void EmitFunctionDetail(StringBuilder sb, NamespaceFunction fn, bool qualified)
    {
        string display = qualified ? fn.QualifiedName : fn.Name;
        string signature = FormatSignature(display, fn);
        sb.Append("#### `").Append(signature).AppendLine("`");
        sb.AppendLine();

        if (fn.Deprecation is not null)
        {
            sb.Append("> _Deprecated — use `").Append(fn.Deprecation.ReplacementQualifiedName).AppendLine("` instead._");
            sb.AppendLine();
        }

        var doc = DocString.Parse(fn.Documentation);
        if (!string.IsNullOrEmpty(doc.Summary))
        {
            sb.AppendLine(doc.Summary);
            sb.AppendLine();
        }

        if (fn.Parameters.Length > 0)
        {
            sb.AppendLine("**Parameters:**");
            sb.AppendLine();
            foreach (var p in fn.Parameters)
            {
                sb.Append("- `").Append(p.Name).Append("`");
                if (!string.IsNullOrEmpty(p.Type))
                    sb.Append(": ").Append(FormatTypeRaw(p.Type));
                if (doc.Params.TryGetValue(p.Name, out var desc) && !string.IsNullOrEmpty(desc))
                    sb.Append(" — ").Append(desc);
                sb.AppendLine();
            }
            sb.AppendLine();
        }

        if (!string.IsNullOrEmpty(fn.ReturnType) || !string.IsNullOrEmpty(doc.Returns))
        {
            sb.Append("**Returns:** ");
            if (!string.IsNullOrEmpty(fn.ReturnType))
                sb.Append(FormatTypeRaw(fn.ReturnType!));
            if (!string.IsNullOrEmpty(doc.Returns))
            {
                if (!string.IsNullOrEmpty(fn.ReturnType)) sb.Append(" — ");
                sb.Append(doc.Returns);
            }
            sb.AppendLine();
            sb.AppendLine();
        }

        if (fn.Throws is not null && fn.Throws.Length > 0)
        {
            sb.AppendLine("**Throws:**");
            sb.AppendLine();
            foreach (var t in fn.Throws)
            {
                sb.Append("- `").Append(t.ErrorType).Append("`");
                if (!string.IsNullOrEmpty(t.Description))
                    sb.Append(" — ").Append(t.Description);
                sb.AppendLine();
            }
            sb.AppendLine();
        }
    }

    private static string FormatSignature(string displayName, NamespaceFunction fn)
    {
        var parts = new List<string>();
        for (int i = 0; i < fn.Parameters.Length; i++)
        {
            var p = fn.Parameters[i];
            bool isVariadicTail = fn.IsVariadic && i == fn.Parameters.Length - 1;
            string name = isVariadicTail ? "..." + p.Name : p.Name;
            if (!string.IsNullOrEmpty(p.Type))
                parts.Add(name + ": " + p.Type);
            else
                parts.Add(name);
        }
        string sig = displayName + "(" + string.Join(", ", parts) + ")";
        if (!string.IsNullOrEmpty(fn.ReturnType))
            sig += " -> " + fn.ReturnType;
        return sig;
    }

    private static string ThrowsCell(ThrowsEntry[]? throws)
    {
        if (throws is null || throws.Length == 0) return "—";
        var names = throws.Select(t => "`" + t.ErrorType + "`").Distinct(StringComparer.Ordinal);
        return string.Join(", ", names);
    }

    // ── Appendices ───────────────────────────────────────────────────────────

    private static void EmitAppendices(StringBuilder sb, IReadOnlyList<NamespaceDefinition> namespaces)
    {
        EmitDeprecationAppendix(sb, namespaces);
        EmitCapabilityAppendix(sb, namespaces);
    }

    private static void EmitDeprecationAppendix(StringBuilder sb, IReadOnlyList<NamespaceDefinition> namespaces)
    {
        var rows = new List<(string Qualified, string Replacement, string Kind)>();
        foreach (var ns in namespaces)
        {
            foreach (var fn in ns.Functions)
            {
                if (fn.Deprecation is not null)
                    rows.Add((fn.QualifiedName, fn.Deprecation.ReplacementQualifiedName, "function"));
            }
            foreach (var c in ns.Constants)
            {
                if (c.Deprecation is not null)
                    rows.Add((c.QualifiedName, c.Deprecation.ReplacementQualifiedName, "constant"));
            }
        }
        rows.Sort((a, b) => StringComparer.Ordinal.Compare(a.Qualified, b.Qualified));

        sb.AppendLine("## Appendix A — Deprecated Members");
        sb.AppendLine();
        if (rows.Count == 0)
        {
            sb.AppendLine("_No deprecated members._");
            sb.AppendLine();
            sb.AppendLine("---");
            sb.AppendLine();
            return;
        }
        sb.AppendLine("Each row is reported by static analysis as `SA0830` when used in a script.");
        sb.AppendLine();
        sb.AppendLine("| Member | Kind | Replacement |");
        sb.AppendLine("| ------ | ---- | ----------- |");
        foreach (var (qualified, replacement, kind) in rows)
        {
            sb.Append("| `").Append(qualified).Append("` | ").Append(kind).Append(" | `").Append(replacement).AppendLine("` |");
        }
        sb.AppendLine();
        sb.AppendLine("---");
        sb.AppendLine();
    }

    private static void EmitCapabilityAppendix(StringBuilder sb, IReadOnlyList<NamespaceDefinition> namespaces)
    {
        sb.AppendLine("## Appendix B — Capability-Gated Namespaces");
        sb.AppendLine();
        sb.AppendLine("Embedded hosts can omit capabilities to disable the matching namespaces. A namespace listed");
        sb.AppendLine("here will not appear in the runtime when its capability is not granted.");
        sb.AppendLine();
        sb.AppendLine("| Namespace | Capability |");
        sb.AppendLine("| --------- | ---------- |");
        var gated = namespaces
            .Where(n => !n.IsGlobal && n.RequiredCapability != StashCapabilities.None)
            .OrderBy(n => n.Name, StringComparer.Ordinal);
        foreach (var ns in gated)
        {
            sb.Append("| `").Append(ns.Name).Append("` | `").Append(ns.RequiredCapability).AppendLine("` |");
        }
        sb.AppendLine();
    }

    // ── Helpers ──────────────────────────────────────────────────────────────

    private static string FormatType(string? type) =>
        string.IsNullOrEmpty(type) ? "—" : "`" + type + "`";

    private static string FormatTypeRaw(string type) => "`" + type + "`";

    private static string InlineCodeIfShort(string value)
    {
        if (string.IsNullOrEmpty(value)) return "—";
        return "`" + value + "`";
    }

    private static string EscapeTableCell(string text)
    {
        if (string.IsNullOrEmpty(text)) return "";
        // Pipes break Markdown table rows; everything else is rendered literally.
        return text.Replace("|", "\\|").Replace("\r", "").Replace("\n", " ");
    }

    private static string HeadingAnchor(string heading)
    {
        // GitHub-style: lowercase, strip everything that isn't a-z, 0-9, space,
        // or hyphen, then convert spaces to hyphens. Backticks vanish, em-dashes
        // contribute nothing and the surrounding spaces collapse into the
        // double-hyphen that the existing TOC already uses.
        var sb = new StringBuilder(heading.Length);
        foreach (char ch in heading.ToLowerInvariant())
        {
            if (char.IsLetterOrDigit(ch)) sb.Append(ch);
            else if (ch == ' ') sb.Append('-');
            else if (ch == '-') sb.Append('-');
            // backticks, em-dash (—), parentheses, etc. are dropped
        }
        return sb.ToString();
    }

    private static string Normalize(string text)
    {
        // Force LF endings, strip trailing whitespace on each line, and end the
        // file with exactly one trailing newline.
        var lines = text.Replace("\r\n", "\n").Split('\n');
        var sb = new StringBuilder(text.Length);
        for (int i = 0; i < lines.Length; i++)
        {
            sb.Append(lines[i].TrimEnd());
            if (i < lines.Length - 1) sb.Append('\n');
        }
        string result = sb.ToString();
        // Collapse any run of trailing newlines to one.
        while (result.EndsWith("\n\n", StringComparison.Ordinal))
            result = result.Substring(0, result.Length - 1);
        if (!result.EndsWith("\n", StringComparison.Ordinal))
            result += "\n";
        return result;
    }
