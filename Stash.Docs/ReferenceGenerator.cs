    // ── Global section ───────────────────────────────────────────────────────

    private static void EmitGlobalSection(StringBuilder sb, NamespaceDefinition? globalNs)
    {
        sb.AppendLine("## Global Functions and Types");
        sb.AppendLine();
        sb.AppendLine("Names in this section are reachable without a namespace qualifier — they live directly in the");
        sb.AppendLine("global scope.");
        sb.AppendLine();

        EmitBuiltInErrorTypes(sb);

        if (globalNs is null)
        {
            sb.AppendLine("---");
            sb.AppendLine();
            return;
        }

        var globalStructs = globalNs.Structs
            .Where(s => !IsErrorTypeStruct(s.Name))
            .OrderBy(s => s.Name, StringComparer.Ordinal)
            .ToArray();
        var globalEnums = globalNs.Enums.OrderBy(e => e.Name, StringComparer.Ordinal).ToArray();
        EmitTypesSection(sb, globalStructs, globalEnums, headingLevel: 3);

        if (globalNs.Functions.Count > 0)
        {
            sb.AppendLine("### Functions");
            sb.AppendLine();
            EmitFunctionSummaryTable(sb, globalNs.Functions, qualified: false);
            sb.AppendLine("### Function Details");
            sb.AppendLine();
            foreach (var fn in globalNs.Functions)
            {
                EmitFunctionDetail(sb, fn, qualified: false);
            }
        }

        sb.AppendLine("---");
        sb.AppendLine();
    }

    private static void EmitBuiltInErrorTypes(StringBuilder sb)
    {
        sb.AppendLine("### Built-in Error Types");
        sb.AppendLine();
        sb.AppendLine("Stash defines a set of built-in named error types. These types are globally visible — no import");
        sb.AppendLine("required. They can be thrown with struct-literal syntax and caught by typed `catch` clauses.");
        sb.AppendLine();
        sb.AppendLine("| Type | Fields | When thrown |");
        sb.AppendLine("| ---- | ------ | ----------- |");

        foreach (var m in BuiltInErrorRegistry.Metadata.Values.OrderBy(m => m.Name, StringComparer.Ordinal))
        {
            sb.Append("| `").Append(m.Name).Append("` | ").Append(FormatErrorFields(m)).Append(" | ").Append(m.Description).AppendLine(" |");
        }
        sb.AppendLine();
        sb.AppendLine("All caught errors expose `.message: string`, `.type: string`, and `.stack: array<string>` in");
        sb.AppendLine("addition to any type-specific fields shown above.");
        sb.AppendLine();
    }

    private static string FormatErrorFields(BuiltInErrorMetadata m)
    {
        // Base fields are always present; extra fields come from the attribute metadata.
        var parts = new List<string> { "`message: string`" };
        for (int i = 0; i < m.Properties.Count; i++)
        {
            string type = i < m.PropertyTypes.Count ? m.PropertyTypes[i] : "any";
            parts.Add("`" + m.Properties[i] + ": " + type + "`");
        }
        return string.Join(", ", parts);
    }

    private static bool IsErrorTypeStruct(string name) => BuiltInErrorRegistry.IsBuiltInName(name);

    // ── Namespace section ────────────────────────────────────────────────────

    private static void EmitNamespace(StringBuilder sb, NamespaceDefinition ns)
    {
        string heading = NamespaceHeading(ns);
        sb.Append("## ").AppendLine(heading);
        sb.AppendLine();
        sb.Append("**Capability:** ").AppendLine(CapabilityLabel(ns.RequiredCapability));

        var aggregatedThrows = AggregateThrows(ns);
        if (aggregatedThrows.Count > 0)
        {
            sb.Append("**Throws:** ");
            sb.AppendLine(string.Join(", ", aggregatedThrows.Select(t => "`" + t + "`")));
        }
        sb.AppendLine();

        EmitTypesSection(sb, ns.Structs, ns.Enums, headingLevel: 3);
        EmitConstantsSection(sb, ns.Constants);

        if (ns.Functions.Count > 0)
        {
            sb.AppendLine("### Functions");
            sb.AppendLine();
            EmitFunctionSummaryTable(sb, ns.Functions, qualified: true);
            sb.AppendLine("### Function Details");
            sb.AppendLine();
            foreach (var fn in ns.Functions)
            {
                EmitFunctionDetail(sb, fn, qualified: true);
            }
        }

        sb.AppendLine("---");
        sb.AppendLine();
    }

    private static string NamespaceHeading(NamespaceDefinition ns)
    {
        // The heading shape ("`name` — Human") drives both display and anchor.
        // Keep it deterministic and tied to the namespace name.
        string human = HumanizeNamespace(ns.Name);
        return $"`{ns.Name}` — {human}";
    }

    private static string HumanizeNamespace(string name)
    {
        // Look up the curated human name; fall back to the namespace identifier
        // so unknown namespaces still render predictably.
        return HumanNames.TryGetValue(name, out var human) ? human : name;
    }

